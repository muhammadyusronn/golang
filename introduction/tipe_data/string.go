package main

import "fmt"

func main() {
	/*
	   String adalah kumpulan dari beberapa karakter
	   Jumlah karakter string bisa 0 dan tidak terhingga
	   Nilai string selalu diawali dengan petik dua (")
	   dan diakhiri petik dua (")

	   Function untuk string
	   Function					Ketarangan
	   len("string")			Menghitung jumlah karakter string
	   "string"[number]			Mengambil karakter pada posisi yang ditentukan oleh number

	*/
	fmt.Println("Firstda")
	fmt.Println("Augustin")

	fmt.Println("Jumlah Karakter dari Firstda = ", len("Firstda"))
	// Memberika nilai byte dari karakter F
	fmt.Println("Firstda"[0])

}
